<?php
/*
|--------------------------------------------------------------------------
|    Require all files required at the instance
|--------------------------------------------------------------------------
 */
use App\Controller as Controller;
use App\Database as Database;
use App\Route as Route;
use Dotenv\Dotenv;

/*
|--------------------------------------------------------------------------
|    Loading env variables !!!
|--------------------------------------------------------------------------
 */
$dotenv = new Dotenv(realpath(__DIR__ . '/..'));
